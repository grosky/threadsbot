def build_user_message(profile: dict, format_name: str, topic: str | None) -> str:
    """Собирает user message из профиля и параметров запроса."""
    parts = [
        "ПРОФИЛЬ АВТОРА:",
        f"- Ниша: {profile.get('niche') or '—'}",
        f"- ЦА: {profile.get('audience') or '—'}",
        f"- Что продаёт: {profile.get('product') or '—'}",
        f"- Ссылка на продукт: {profile.get('product_link') or '—'}",
        f"- Tone of voice: {profile.get('tone') or 'ЭКСПЕРТНЫЙ'}",
        f"- Личные факты: {profile.get('facts') or '—'}",
        f"- Главные боли ЦА: {profile.get('pains') or '—'}",
        f"- Social proof: {profile.get('social_proof') or '—'}",
        "",
        f"ФОРМАТ ПОСТА: {format_name}",
    ]
    if topic:
        parts.append(f"\nТЕМА: {topic}")
    parts.append("\nСоздай 3 разных варианта поста под этот профиль и формат.")
    return "\n".join(parts)


