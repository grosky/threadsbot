def build_user_message(
    profile: dict,
    topic: str | None,
    length: str = "long",
) -> str:
    """Собирает user message из профиля и параметров запроса.

    Формат НЕ передаётся — Gemini сам выбирает 3 разных формата.

    length:
    - "short" → каждый вариант ≤ 450 символов (умещается в один пост Threads)
    - "long" → развёрнутый пост на 1500-2500 символов
    """
    parts = [
        "ПРОФИЛЬ АВТОРА:",
        f"- Ниша: {profile.get('niche') or '—'}",
        f"- ЦА: {profile.get('audience') or '—'}",
        f"- Что продаёт: {profile.get('product') or '—'}",
        f"- Ссылка на продукт: {profile.get('product_link') or '—'}",
        f"- Tone of voice: {profile.get('tone') or 'ЭКСПЕРТНЫЙ'}",
        f"- Личные факты: {profile.get('facts') or '—'}",
        f"- Главные боли ЦА: {profile.get('pains') or '—'}",
        f"- Social proof: {profile.get('social_proof') or '—'}",
    ]
    if topic:
        parts.append(f"\nТЕМА: {topic}")
    else:
        parts.append(
            "\nТЕМА: на твой выбор — подбери острую и злободневную "
            "под нишу/ЦА автора"
        )

    if length == "short":
        parts.append(
            "\nДЛИНА: КОРОТКИЙ ПОСТ.\n"
            "ВАЖНО — каждый из 3 вариантов ≤ 450 символов "
            "(должен умещаться в ОДИН пост Threads).\n"
            "Стиль для коротких:\n"
            "- Только хук + 2-3 убойные строки\n"
            "- БЕЗ нумерованных пунктов, БЕЗ заголовков, БЕЗ Вердикта-цитаты\n"
            "- БЕЗ CTA, БЕЗ ссылок на t.me/каналы/продукт — даже если в "
            "профиле автора есть product_link, в коротких НЕ упоминай\n"
            "- БЕЗ призыва «жми/переходи/забери» в конце\n"
            "- Каждое слово работает. Никакого ядра-списка.\n"
            "- Заканчивай мощной строкой / афоризмом / провокацией, "
            "а не призывом к действию"
        )
    else:
        parts.append(
            "\nДЛИНА: РАЗВЁРНУТЫЙ ТРЕД (1500-2500 символов).\n"
            "Используй полную структуру: хук → анти-тезис → нумерованное ядро → "
            "финальный пункт с нативной продажей."
        )

    parts.append(
        "\nСоздай 3 РАЗНЫХ варианта в 3 РАЗНЫХ форматах. "
        "В каждом варианте проставь поле 'format' (манифест / разбор / "
        "контринтуитивный / история / метод_известного)."
    )
    return "\n".join(parts)


