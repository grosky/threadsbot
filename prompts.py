def build_product_builder_message(
    profile: dict,
    expertise_text: str,
    effort_level: str,
) -> str:
    """User message для генерации продуктовых идей."""
    effort_labels = {
        "low": "минимум — хочет пассивные продукты, без активной работы",
        "medium": "средне — готов вкладывать несколько часов в день",
        "high": "максимум — готов работать с клиентами лично, проводить созвоны",
    }
    effort_desc = effort_labels.get(effort_level, "не указано")
    parts = [
        "ПРОФИЛЬ АВТОРА (из онбординга):",
        f"- Ниша: {profile.get('niche') or '—'}",
        f"- ЦА: {profile.get('audience') or '—'}",
        f"- Что продаёт сейчас: {profile.get('product') or 'ничего пока'}",
        f"- Ссылка на продукт: {profile.get('product_link') or '—'}",
        f"- Tone of voice: {profile.get('tone') or 'ЭКСПЕРТНЫЙ'}",
        f"- Главные боли ЦА: {profile.get('pains') or '—'}",
        f"- Social proof: {profile.get('social_proof') or '—'}",
        "",
        f"ЭКСПЕРТИЗА АВТОРА: {expertise_text or 'не указано, опирайся на нишу'}",
        f"ГОТОВНОСТЬ ВКЛАДЫВАТЬСЯ: {effort_desc}",
        "",
        "Предложи 5 продуктовых идей под этого автора. Каждая с ценой "
        "и обоснованием. Сортируй от низкой цены к высокой. "
        "Верни JSON по схеме.",
    ]
    return "\n".join(parts)
